const calendar = document.getElementById("calendar");

const month = (onemonth = []) => {
  let innerDays = "";
    onemonth.forEach((months) => {
    const calendarMonth = document.createElement("div");
    calendarMonth.classList.add("col", "m-3");
    calendarMonth.innerHTML = `<h4 class="text-center">${months.month}</h4>`;
    calendar.appendChild(calendarMonth);

    const tableMonth = document.createElement("table");
    tableMonth.classList.add(
      "table",
      "table-borderless",
      "vertical-align",
      "table-sm"
    );
    calendarMonth.appendChild(tableMonth);

    const theadTable = document.createElement("thead");
    theadTable.innerHTML = `<tr>
    <th class="text-center">Sun</th>
    <th class="text-center">Mon</th>
    <th class="text-center">Tue</th>
    <th class="text-center">Wed</th>
    <th class="text-center">Thu</th>
    <th class="text-center">Fri</th>
    <th class="text-center">Sat</th>
    </tr>`;
    tableMonth.appendChild(theadTable);

    const tbodyTable = document.createElement("tbody");
    tableMonth.appendChild(tbodyTable);

    let j = 0;
    for (let i = 0; i < 6; i++) {
      const trTable = document.createElement("tr");
      tbodyTable.appendChild(trTable);
      for (let i = 0; i < 7; i++, j++) {
        const tdTable = document.createElement("td");
        tdTable.classList.add("text-center");
        if (months.days[j] === "") {
          trTable.appendChild(tdTable);
          continue;
        }
        if (!months.days[j]) break;
        const btnDay = document.createElement("button");
        btnDay.classList.add("btnDay", "btn", "p-1");
        btnDay.id = "btnByDay";
        btnDay.innerHTML = `<div>${months.days[j]}</div>`;
        trTable.appendChild(tdTable);
        tdTable.appendChild(btnDay);
      }
    }
  });
};
